UA_HDR = {"User-Agent": UA}
_last_req = [0.0]

def _throttle():
    wait = _last_req[0] + 0.5 - time.time()
    if wait > 0:
        time.sleep(wait)
    _last_req[0] = time.time()

def http_get(url, headers=None, timeout=20):
    for attempt in range(3):
        _throttle()
        try:
            req = urllib.request.Request(url, headers=headers or UA_HDR)
            with urllib.request.urlopen(req, timeout=timeout) as r:
                return r.read(), r.headers
        except Exception as e:
            if attempt < 2:
                time.sleep(1.5 * (attempt + 1))
            else:
                print(f"  [skip] {url[:80]} -> {e}")
    return None, None

def get_crumb():
    """야후 쿠키+크럼 확보 (시총 조회용 quote API에 필요)"""
    try:
        _throttle()
        req = urllib.request.Request("https://fc.yahoo.com", headers=UA_HDR)
        cookie = ""
        try:
            with urllib.request.urlopen(req, timeout=15) as r:
                cookie = r.headers.get("Set-Cookie", "")
        except urllib.error.HTTPError as e:  # fc.yahoo.com은 404를 주지만 쿠키는 준다
            cookie = e.headers.get("Set-Cookie", "")
        cookie = cookie.split(";")[0] if cookie else ""
        if not cookie:
            return None, None
        hdr = {"User-Agent": UA, "Cookie": cookie}
        raw, _ = http_get("https://query1.finance.yahoo.com/v1/test/getcrumb", headers=hdr)
        if not raw:
            return None, None
        crumb = raw.decode("utf-8").strip()
        if not crumb or "<" in crumb:
            return None, None
        return cookie, crumb
    except Exception as e:
        print(f"  [crumb-fail] {e}")
        return None, None

def fetch_quotes(symbols, cookie, crumb):
    """v7 quote API 배치 조회 → {symbol: {mcap, currency, price}}"""
    out = {}
    hdr = {"User-Agent": UA, "Cookie": cookie}
    for i in range(0, len(symbols), 40):
        batch = symbols[i:i + 40]
        url = ("https://query1.finance.yahoo.com/v7/finance/quote?symbols="
               + urllib.parse.quote(",".join(batch))
               + "&fields=marketCap,currency,regularMarketPrice&crumb="
               + urllib.parse.quote(crumb))
        raw, _ = http_get(url, headers=hdr)
        if not raw:
            continue
        try:
            for q in json.loads(raw)["quoteResponse"]["result"]:
                out[q.get("symbol")] = {
                    "mcap": q.get("marketCap") or 0,
                    "currency": q.get("currency") or "USD",
                    "price": q.get("regularMarketPrice") or 0,
                }
        except Exception as e:
            print(f"  [quote-parse-fail] batch {i}: {e}")
        print(f"  quotes {min(i + 40, len(symbols))}/{len(symbols)}")
    return out

def fetch_fx_rate(ccy):
    """1 {ccy} = ? USD"""
    if ccy == "USD":
        return 1.0
    url = (f"https://query1.finance.yahoo.com/v8/finance/chart/"
           f"{urllib.parse.quote(ccy + 'USD=X')}?range=5d&interval=1d")
    raw, _ = http_get(url)
    if not raw:
        return None
    try:
        res = json.loads(raw)["chart"]["result"][0]
        closes = [c for c in res["indicators"]["quote"][0]["close"] if c]
        return float(closes[-1]) if closes else None
    except Exception:
        return None

def main():
    print(f"=== fetch_universe.py 시작 ({datetime.now(KST).strftime('%Y-%m-%d %H:%M KST')}) ===")
    meta = {t: (n, s) for t, n, s in CANDIDATES}
    symbols = list(meta.keys())

    cookie, crumb = get_crumb()
    if not crumb:
        print("[경고] 크럼 확보 실패 — 기존 명단 유지 후 종료")
        if not os.path.exists(OUT):
            sys.exit(1)
        sys.exit(0)
    print(f"  crumb OK")

    quotes = fetch_quotes(symbols, cookie, crumb)
    if len(quotes) < len(symbols) * 0.5:
        print(f"[경고] 시총 조회 {len(quotes)}/{len(symbols)}종만 성공")
        if len(quotes) < 50:
            print("[경고] 성공 종목이 너무 적음 — 기존 명단 유지 후 종료")
            if not os.path.exists(OUT):
                sys.exit(1)
            sys.exit(0)

    # 통화별 환율 (GBp = 펜스 → GBP/100)
    ccys = {q["currency"] for q in quotes.values()}
    fx = {"USD": 1.0}
    for c in sorted(ccys):
        base = "GBP" if c == "GBp" else c
        if base not in fx:
            rate = fetch_fx_rate(base)
            if rate:
                fx[base] = rate
                print(f"  FX {base}USD = {rate:.6f}")

    rows = []
    for sym, q in quotes.items():
        if not q["mcap"]:
            continue
        c = q["currency"]
        base = "GBP" if c == "GBp" else c
        rate = fx.get(base)
        if not rate:
            continue
        mcap_usd = q["mcap"] * rate  # marketCap은 현지통화 기준 (GBp여도 GBP 단위)
        name, sector = meta[sym]
        rows.append({"ticker": sym, "name": name, "sector": sector,
                     "mcap_usd": int(mcap_usd)})

    rows.sort(key=lambda r: -r["mcap_usd"])
    top = rows[:TOP_N]
    data = {
        "updated": datetime.now(KST).strftime("%Y-%m-%d %H:%M KST"),
        "count": len(top),
        "stocks": top,
    }
    os.makedirs(os.path.dirname(OUT), exist_ok=True)
    with open(OUT, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=1)
    print(f"=== 완료: 후보 {len(symbols)} → 시총 확보 {len(rows)} → TOP{len(top)} 저장 ===")
    if top:
        print(f"  1위: {top[0]['name']} ${top[0]['mcap_usd']/1e12:.2f}T")

if __name__ == "__main__":
    main()
